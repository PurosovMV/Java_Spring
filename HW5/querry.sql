DROP TABLE book;
DROP TABLE issue;
DROP TABLE reader;

CREATE TABLE IF NOT EXISTS book
(
    id    BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title VARCHAR(256) NOT NULL
);

