CREATE TABLE IF NOT EXISTS issue
(
    id          BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    book_id     BIGINT NOT NULL,
    reader_id   BIGINT NOT NULL,
    issued_at   DATE,
    returned_at DATE
);
